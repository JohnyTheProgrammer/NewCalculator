package hometask5;

public class SwitchSeason {
    public static void swSeasons(int number_of_month){
        switch(number_of_month){
            case(3):
            case(4):
            case(5):
                System.out.println("Its Spring Now!");
                break;
            case(6):
            case(7):
            case(8):
                System.out.println("Its Summer time!!!");
                break;
            case(9):
            case(10):
            case(11):
                System.out.println("Time to go to school");
                break;
            case(12):
            case(1):
            case(2):
                System.out.println("Merry Christmas Deary!!!)");
                break;
        }


    }
}
