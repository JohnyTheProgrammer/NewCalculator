package hometask5;

public class IfSeason {
    public static void ifElseSeasons(int number_of_month){
        if(number_of_month == 3 || number_of_month == 4 || number_of_month == 5) {
            System.out.println("Its Spring Now!");
        }
        else if(number_of_month == 6 || number_of_month == 7 || number_of_month == 8) {
                    System.out.println("Its Summer time!!!");
        }
        else if(number_of_month == 9 || number_of_month == 10 || number_of_month == 11) {
                System.out.println("Time to go to school");
        }
        else if(number_of_month == 1 || number_of_month == 2 || number_of_month == 12) {
            System.out.println("Merry Christmas Deary!!!)");
        }
    }
}


